const normalizeTenantIdValue = (value, fallbackTenantId = DEFAULT_TENANT_ID) => {
  if (value == null) {
    return fallbackTenantId;
  }

  if (typeof value === 'number') {
    return value === 1 ? fallbackTenantId : String(value);
  }

  if (typeof value === 'string') {
    const trimmed = value.trim();
    if (!trimmed) return fallbackTenantId;
    if (/^0+$/.test(trimmed)) return fallbackTenantId;
    if (/^[0-9]+$/.test(trimmed)) {
      return trimmed === '1' ? fallbackTenantId : trimmed;
    }
    return trimmed;
  }

  return fallbackTenantId;
};

const normalizeTenantScopedSeedData = (seedMap, fallbackTenantId = DEFAULT_TENANT_ID) => {
  if (!seedMap || typeof seedMap !== 'object') {
    return seedMap;
  }

  return Object.fromEntries(
    Object.entries(seedMap).map(([entityName, records]) => {
      if (!Array.isArray(records)) {
        return [entityName, records];
      }

      return [
        entityName,
        records.map((record) => {
          if (!record || typeof record !== 'object') {
            return record;
          }
          const next = { ...record };
          if ('tenant_id' in next) {
            next.tenant_id = normalizeTenantIdValue(next.tenant_id, fallbackTenantId);
          }
          if ('tenant' in next && typeof next.tenant !== 'string') {
            next.tenant = normalizeTenantIdValue(next.tenant, fallbackTenantId);
          }
          if ('tenantId' in next) {
            next.tenantId = normalizeTenantIdValue(next.tenantId, fallbackTenantId);
          }
          return next;
        }),
      ];
    })
  );
};

