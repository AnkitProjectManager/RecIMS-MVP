const normalizedSeedData = normalizeTenantScopedSeedData(fallbackSeedData, DEFAULT_TENANT_ID);

export function ensureSeedDataLoaded() {
  if (typeof window === 'undefined') return;
  try {
    const shouldForceReset = process.env.NODE_ENV === 'development';
    const currentSignature = getSeedSignature();
    const previousSignature = window.localStorage.getItem(SEED_BUILD_KEY);

    if (shouldForceReset) {
      window.localStorage.removeItem(SEED_FLAG_KEY);
    } else if (previousSignature && previousSignature !== currentSignature) {
      window.localStorage.removeItem(SEED_FLAG_KEY);
    }

    const existingVersion = window.localStorage.getItem(SEED_FLAG_KEY);
    if (existingVersion === SEED_DATA_VERSION && !shouldForceReset) {
      if (previousSignature !== currentSignature) {
        window.localStorage.setItem(SEED_BUILD_KEY, currentSignature);
      }
      return;
    }

  recims.seedFallbackData(normalizedSeedData);
    window.localStorage.setItem(SEED_FLAG_KEY, SEED_DATA_VERSION);
    window.localStorage.setItem(SEED_BUILD_KEY, currentSignature);
  } catch (error) {
    console.warn('[seedData] Failed to seed fallback data', error);
  }
}